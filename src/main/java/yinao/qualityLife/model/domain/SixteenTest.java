package yinao.qualityLife.model.domain;

import java.util.List;

public class SixteenTest {
	
	private String no ; 
	private String question ; 
	private String type ; 
	private List<SixteenOption> list ;
	
	public SixteenTest() {}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getQuestion() {
		return question;
	}

	public void setQuestion(String question) {
		this.question = question;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public List<SixteenOption> getList() {
		return list;
	}

	public void setList(List<SixteenOption> list) {
		this.list = list;
	}
	
	

}
