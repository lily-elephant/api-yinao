package yinao.qualityLife.model.domain;

public class AnsewerCompare {
	
	private String eid ; 
	private String true_oid ; 
	private String user_oid ; 
	private String username ;
	private String create_time ; 
	private String state ; 
	
	public AnsewerCompare() {}

	public String getEid() {
		return eid;
	}

	public void setEid(String eid) {
		this.eid = eid;
	}

	public String getTrue_oid() {
		return true_oid;
	}

	public void setTrue_oid(String true_oid) {
		this.true_oid = true_oid;
	}

	public String getUser_oid() {
		return user_oid;
	}

	public void setUser_oid(String user_oid) {
		this.user_oid = user_oid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}
	
	

}
