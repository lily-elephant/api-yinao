package yinao.qualityLife.model.domain;

public class AppointmentMoney {
	
	private String amid ; 
	private int appointmentMoney ; 
	private String state ; 
	private String create_time ; 
	private String update_time ; 
	private String unit ; 
	
	public AppointmentMoney() {}

	public String getAmid() {
		return amid;
	}

	public void setAmid(String amid) {
		this.amid = amid;
	}

	public int getAppointmentMoney() {
		return appointmentMoney;
	}

	public void setAppointmentMoney(int appointmentMoney) {
		this.appointmentMoney = appointmentMoney;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

	public String getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	
	

}
